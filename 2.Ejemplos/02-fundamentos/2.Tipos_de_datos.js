// Ejemplos de tipos de datos en JavaScript

// Número (Number)
let numero = 10;
console.log(typeof numero);  // Salida: number

// Texto (String)
let texto = "Hola Mundo";
console.log(typeof texto);  // Salida: string

// Booleano (Boolean)
let esActivo = true;
console.log(typeof esActivo);  // Salida: boolean

// Nulo (Null)
let nulo = null;
console.log(typeof nulo);  // Salida: object (un error histórico en JavaScript)

// Indefinido (Undefined)
let indefinido;
console.log(typeof indefinido);  // Salida: undefined

// BigInt (para números grandes)
let numeroGrande = 1234567890123456789012345678901234567890n;
console.log(typeof numeroGrande);  // Salida: bigint
