// Ejemplos de operadores en JavaScript

// Operadores Aritméticos
let a = 5, b = 3;
console.log(a + b);  // Suma: 8
console.log(a - b);  // Resta: 2
console.log(a * b);  // Multiplicación: 15
console.log(a / b);  // División: 1.666666...
console.log(a % b);  // Módulo: 2
console.log(a ** b); // Exponentiación: 125

// Operadores de Comparación
console.log(a == b);  // Igualdad: false
console.log(a === b); // Igualdad estricta: false
console.log(a != b);  // Desigualdad: true
console.log(a > b);   // Mayor que: true
console.log(a < b);   // Menor que: false

// Operadores Lógicos
let x = true, y = false;
console.log(x && y);  // AND: false
console.log(x || y);  // OR: true
console.log(!x);      // NOT: false
