// Ejemplo de declaración de variables

// Usando let (variable que puede cambiar)
let nombre = "Juan";
nombre = "Carlos";  // Cambio de valor
console.log(nombre);  // Salida: Carlos

// Usando const (variable constante)
const edad = 25;
// edad = 30;  // Esto dará un error porque const no permite cambiar su valor.
console.log(edad);  // Salida: 25

// Usando var (antiguo, no recomendado en la actualidad)
var pais = "España";
pais = "México";  // Cambio de valor
console.log(pais);  // Salida: México
