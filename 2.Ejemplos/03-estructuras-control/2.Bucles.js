// Ejemplo de bucles en JavaScript

// Usando for
for (let i = 0; i < 5; i++) {
    console.log("Número: " + i);
}

// Usando while
let j = 0;
while (j < 5) {
    console.log("Número: " + j);
    j++;
}

// Usando do...while
let k = 0;
do {
    console.log("Número: " + k);
    k++;
} while (k < 5);
